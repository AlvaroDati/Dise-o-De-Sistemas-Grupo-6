package proyectoInversiones;


public class Cuenta{
	private double ebitda, fds, freeCashFlow, ingresoNetoDisc,ingresoNetoCont;
	Cuenta(){
		
	}

	public double getEbitda() {
		return ebitda;
	}

	public void setEbitda(double ebitda) {
		this.ebitda = ebitda;
	}

	public double getFds() {
		return fds;
	}

	public void setFds(double fds) {
		this.fds = fds;
	}

	public double getFreeCashFlow() {
		return freeCashFlow;
	}

	public void setFreeCashFlow(double freeCashFlow) {
		this.freeCashFlow = freeCashFlow;
	}

	public double getIngresoNetoDisc() {
		return ingresoNetoDisc;
	}

	public void setIngresoNetoDisc(double ingresoNetoDisc) {
		this.ingresoNetoDisc = ingresoNetoDisc;
	}

	public double getIngresoNetoCont() {
		return ingresoNetoCont;
	}

	public void setIngresoNetoCont(double ingresoNetoCont) {
		this.ingresoNetoCont = ingresoNetoCont;
	}
	
	
 }